package EJEMPLOS;

/**
 * La clase Calculadora realiza operaciones matemáticas básicas.
 */
public class ejemplo1 {

    /**
     * Suma dos números enteros.
     *
     * @param num1 El primer número a sumar.
     * @param num2 El segundo número a sumar.
     * @return La suma de num1 y num2.
     */
    public int sumar(int num1, int num2) {
        return num1 + num2;
    }

    /**
     * Resta dos números enteros.
     *
     * @param num1 El número del que se va a restar.
     * @param num2 El número que se va a restar.
     * @return La diferencia de num1 y num2.
     */
    public int restar(int num1, int num2) {
        return num1 - num2;
    }
}
